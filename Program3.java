class Program3 {
    public static void main(String[] args) {
        int a=6;
        System.out.print("post inc  ");
        System.out.println(a++);
        System.out.print("pre inc  ");
        System.out.println(++a);
        System.out.print("pre dec  ");
        System.out.println(--a);
        System.out.print("post dec  ");
        System.out.println(a--);


    }
    
}